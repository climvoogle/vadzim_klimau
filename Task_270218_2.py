matrix = [[10.1,20.2,30.3,40.4],[50.5,60.6,70.7,80.8],[90.9,100.1,110.1,120.1]]

mysum = 0

p1 = int(input('Введите значение p1: '))
p2 = int(input('Введите значение p2: '))

for i in matrix:
    for j in i:
        if p1 <= j <= p2:
            mysum += 1

print('Количества элементов матрицы, удовлетворяющих условию: ', mysum)